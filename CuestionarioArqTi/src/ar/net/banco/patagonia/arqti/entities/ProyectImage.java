package ar.net.banco.patagonia.arqti.entities;

public class ProyectImage {
	private String imageName;
	private String proyectName;
	private Integer proyectoImageId;
	
	
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getProyectName() {
		return proyectName;
	}
	public void setProyectName(String proyectName) {
		this.proyectName = proyectName;
	}
	public Integer getProyectoImageId() {
		return proyectoImageId;
	}
	public void setProyectoImageId(Integer proyectoImageId) {
		this.proyectoImageId = proyectoImageId;
	}
	

}
